    private void idtextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_idtextMouseClicked
        // TODO add your handling code here:
       Connection conn = null;
        try {
            conn=ConnUtil.getConnection();
            Statement stmt=conn.createStatement();
           // stmt.execute("use library");
             ResultSet rs1=stmt.executeQuery("select max(card_id) as max from borrower;");
           while (rs1.next()){
            int card_id = rs1.getInt("max")+1;
            idtext.setText(Integer.toString(card_id));
              idtext.setEditable(false);
           }
              stmt.close();
        } catch (SQLException ex) {
            Logger.getLogger(AddBorrower.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            ConnUtil.closeConnection(conn);
            
        } 
    }//GEN-LAST:event_idtextMouseClicked
   
    boolean checkValidity(String fname1,String lname1,String phone1,String Ssn1){
    
    
        if(fname1.matches(".*[0-9].*") ){
             
             JOptionPane.showMessageDialog(this,"Enter valid first name ","Error",JOptionPane.ERROR_MESSAGE);
             fname.setText("");
             return false;
             }
        else if(lname1.matches(".*[0-9].*")){
            JOptionPane.showMessageDialog(this,"Enter valid last name ","Error",JOptionPane.ERROR_MESSAGE);
            lname.setText("");
        
        }
        else if (phone1.matches(".*[a-zA-Z].*")){
            JOptionPane.showMessageDialog(this,"Enter valid phone number ","Error",JOptionPane.ERROR_MESSAGE);
            phone.setText("");
        }
        else if(Ssn1.matches(".*[a-zA-Z].*")){
            JOptionPane.showMessageDialog(this,"Enter valid SSN ","Error",JOptionPane.ERROR_MESSAGE);
            Ssn.setText("");
        }
        else{
       return true;
        }
        return true;
    }    
